#include<iostream>
#include<vector>
using namespace std;

int main(){
  string s,result="";
  cout<<"Enter a string: \n";
  cin>>s;
  int hash[s.length()]={0};
  for(int i=0;i<s.length();i++){
    //cout<<s[i]<<" ";
    if(hash[s[i]] == 0){
      hash[s[i]]=1;
      result+=s[i];
    }
  }
  cout<<result<<endl;
  for(int i=0;i<s.length();i++)
   cout<<hash[s[i]]<<" ";

}
  























































































































































































































































